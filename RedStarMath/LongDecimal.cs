	public static explicit operator float(LongDecimal value) => (float)(double)value;
	public static explicit operator double(LongDecimal value)
	{
		if (Mpir.MpzCmp(value.m, value.ZeroMantissa) == 0)
			return 0d;
		else if (Mpir.MpzCmp(value.m, value.PositiveInfinityMantissa) == 0)
			return double.PositiveInfinity;
		else if (Mpir.MpzCmp(value.m, value.NegativeInfinityMantissa) == 0)
			return double.NegativeInfinity;
		else if (Mpir.MpzCmp(value.m, value.NaNMantissa) == 0)
			return double.NaN;
		if (!DoublePowersInitialized)
			lock (lockObj)
				if (!DoublePowersInitialized)
					InitializeDoublePowers();
		if (value > DoubleMax)
			return double.PositiveInfinity;
		else if (value < DoubleMin)
			return double.NegativeInfinity;
		else if (value.Abs() < DoubleEpsilon)
			return 0d;
		var negative = Mpir.MpzCmpSi(value.m, 0) < 0;
		if (negative)
			value = -value;
		var negativeExponent = (value.m & 1) != 0;
		var (lo, hi) = negativeExponent ? (0, 1075) : (1076, DoublePowers.Length - 1);
		while (lo <= hi)
		{
			var mid = lo + (hi - lo) / 2;
			var compared = value.CompareTo(DoublePowers[mid]);
			if (compared == 0)
			{
				hi = mid;
				break;
			}
			else if (compared < 0)
				hi = mid - 1;
			else
				lo = mid + 1;
		}
		var eAfterCast = hi - 53;
		var exponent = (ulong)Math.Max(eAfterCast, 0);
		LongDecimal @base;
		if (value >= 1)
			@base = DivideInternal(value, DoublePowers[hi].GetWithOtherML(value.MantissaLength), value.MantissaLength);
		else
			@base = DivideInternal(DoublePowers[hi].GetWithOtherML(value.MantissaLength).ReciprocInternal(),
				value.ReciprocInternal(), value.MantissaLength);
		var mantissa = @base.m >> 1;
		mantissa += value.MantissaOverflow;
		var powerOfFive = MpuTPowerOf5(value.MantissaLength);
		mantissa = mantissa.Divide(powerOfFive, out var remainder);
		if (Mpir.MpzCmp(remainder, powerOfFive >> 1) >= 0)
			mantissa++;
		var binaryMantissaOverflow = MpzT.One << value.MantissaLength;
		mantissa -= binaryMantissaOverflow;
		if (exponent == 0)
			mantissa = (binaryMantissaOverflow + mantissa).ShiftRightRound(value.MantissaLength - eAfterCast - 51);
		else
			mantissa = mantissa.ShiftRightRound(value.MantissaLength - 52);
		return BitConverter.UInt64BitsToDouble((negative ? 0x8000000000000000 : 0) + (exponent << 52) + (ulong)mantissa);
	}

	public static explicit operator decimal(LongDecimal value)
	{
		if (Mpir.MpzCmp(value.m, value.ZeroMantissa) == 0)
			return decimal.Zero;
		else if (Mpir.MpzCmp(value.m, value.ShiftedMantissaOverflow) > 0)
			throw new OverflowException(SpecialValuesConversionError);
		else if ((value.m & 1) != 0
			&& (value.e > 28 || value.e == 28 && Mpir.MpzCmpabs(value.m, value.MantissaOverflow * 8) < 0))
			return decimal.Zero;
		else if (value > DecimalMax || value < DecimalMin)
			throw new OverflowException("Ошибка, слишком большое (положительное или отрицательное) число"
				+ " для преобразования в decimal!");
		var mantissa = value.m >> 1;
		if (Mpir.MpzCmpSi(mantissa, 0) < 0)
			mantissa = ~mantissa;
		mantissa += value.MantissaOverflow;
		var mantissa2 = mantissa.ShiftRightRoundDec(value.MantissaLength - 28);
		byte exponent;
		ProcessMantissa();
		if (mantissa2 > decimal.MaxValue)
		{
			mantissa2 = mantissa.ShiftRightRoundDec(value.MantissaLength - 27);
			ProcessMantissa(1);
		}
		int i1 = (int)mantissa2, i2 = (int)(mantissa2 >> BitsPerInt), i3 = (int)(mantissa2 >> BitsPerInt * 2);
		return new(i1, i2, i3, Mpir.MpzCmpSi(value.m, 0) < 0, exponent);
		void ProcessMantissa(int offset = 0)
		{
			if ((value.m & 1) != 0)
			{
				exponent = (byte)(value.e + 1);
				while (true)
				{
					var quotient = mantissa2.Divide(10, out var remainder);
					if (remainder != 0)
						break;
					mantissa2 = quotient;
				}
				exponent += (byte)(mantissa2.DecLength - 1);
				if (exponent > 28)
				{
					mantissa2 = mantissa.ShiftRightRoundDec(mantissa.DecLength - mantissa2.DecLength + exponent - 28);
					exponent = 28;
				}
			}
			else
			{
				exponent = (byte)(new UnsignedLongDecimal(28) - value.e - offset);
				while (true)
				{
					var quotient = mantissa2.Divide(10, out var remainder);
					if (remainder != 0 || exponent == 0)
						break;
					mantissa2 = quotient;
					exponent--;
				}
			}
		}
	}

	public static explicit operator MpzT(LongDecimal value)
	{
		if (Mpir.MpzCmp(value.m, value.ShiftedMantissaOverflow) > 0 || (value.m & 1) != 0 || value.e > int.MaxValue)
			return 0;
		var eAfterCast = (int)value.e;
		if (eAfterCast <= value.MantissaLength)
		{
			var mantissa = value.m >> 1;
			if (Mpir.MpzCmpSi(value.m, 0) < 0)
				mantissa = ~mantissa;
			if (Mpir.MpzCmpSi(value.m, 0) >= 0 || Mpir.MpzCmpSi(mantissa, 0) != 0)
				mantissa = (value.MantissaOverflow + mantissa).ShiftRightRoundDec(value.MantissaLength - eAfterCast);
			if (Mpir.MpzCmpSi(value.m, 0) < 0)
				mantissa = ~mantissa;
			return mantissa;
		}
		else
			return value.m.ShiftLeftDec(eAfterCast - value.MantissaLength);
	}

	public static explicit operator MpuT(LongDecimal value)
	{
		if (value.e is null)
			return new(value.m);
		else if (value.e > int.MaxValue)
			return MpuT.Zero;
		var eAfterCast = (int)value.e;
		if (eAfterCast <= value.MantissaLength)
			return (MpuT)(value.MantissaOverflow + (value.m >> 1)).ShiftRightRoundDec(value.MantissaLength - eAfterCast);
		else
			return (MpuT)value.m.ShiftLeftDec(eAfterCast - value.MantissaLength);
	}

