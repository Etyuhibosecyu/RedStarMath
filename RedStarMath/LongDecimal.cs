	{
		var truncated = Truncate();
		var frac = this - truncated;
		if (frac < 0)
			frac++;
		if (frac > 0.5)
			return Ceiling();
		else if (frac < 0.5)
			return Floor();
		else
			return truncated + Sign;
	}

