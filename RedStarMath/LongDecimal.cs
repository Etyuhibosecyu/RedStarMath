	/// Другими словами, возвращает целую часть данного числа, отбрасывая дробную.
	/// </summary>
	/// <returns>
	/// Для нуля - ноль;<br />
	/// для плюс бесконечности - плюс бесконечность;<br />
	/// для минус бесконечности - минус бесконечность;<br />
