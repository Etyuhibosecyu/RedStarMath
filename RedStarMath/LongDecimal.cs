	private LongDecimal SquareInternal()
	{
		var mantissaOverflow = MpuTPowerOf10(MantissaLength);
		var product = (mantissaOverflow + (m >> 1)).Square();
		var shiftAmount = product.DecLength - MantissaLength - 1;
		var shifted = product.ShiftRightRoundDec(shiftAmount);
		if (Mpir.MpzCmp(shifted, mantissaOverflow << 1) == 0)
			shiftAmount++;
		return new(shifted - mantissaOverflow << 1, e * 2 + (shiftAmount - MantissaLength), MantissaLength);
	}

	/// <summary>
	/// Вычисляет тангенс данного числа.
	/// </summary>
	/// <returns>
	/// Для нуля - ноль;<br />
	/// для плюс бесконечности, минус бесконечности и неопределенности - неопределенность;<br />
	/// для чисел, модуль которых больше <see cref="Pi"/> &lt;&lt; <see cref="MantissaLength"/> + 1 - неопределенность;<br />
	/// в остальных случаях - тангенс данного числа.
	/// </returns>
	public LongDecimal Tan()
	{
		var cos = Cos();
		var tau = Tau.GetWithOtherML(MantissaLength);
		var result = Sqrt(1 - cos.Square()) / cos;
		if (this - Floor(this / tau) * tau >= tau / 2)
			result = -result;
		return result;
	}

