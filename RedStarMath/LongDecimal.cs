	private LongDecimal CosInternal()
	{
		var frac = Reciproc().SquareInternal();
		var rowSum = AddInternal(new(MpzT.Zero, UnsignedLongDecimal.Zero, MantissaLength),
			-frac.Reciproc() / 2, MantissaLength);
		LongDecimal factorial = new(MantissaOverflow << 1, UnsignedLongDecimal.Zero, MantissaLength), prev;
		var fracExponent = frac;
		var i = 3L;
		do
		{
			prev = rowSum;
#pragma warning disable IDE0079 // Удалить ненужное подавление
#pragma warning disable S1121
			rowSum = AddInternal(rowSum,
				AddInternal(MultiplyInternal(fracExponent = MultiplyInternal(fracExponent, frac, MantissaLength),
				factorial = MultiplyInternal(factorial, new(i++ * i++, MantissaLength), MantissaLength),
				MantissaLength).ReciprocInternal(),
				-MultiplyInternal(fracExponent = MultiplyInternal(fracExponent, frac, MantissaLength),
				factorial = MultiplyInternal(factorial, new(i++ * i++, MantissaLength), MantissaLength),
				MantissaLength).ReciprocInternal(), MantissaLength), MantissaLength);
#pragma warning restore S1121
#pragma warning restore IDE0079 // Удалить ненужное подавление
		} while (prev.e != rowSum.e || Mpir.MpzCmpabsUi((prev.m >> 1) - (rowSum.m >> 1), 1) > 0);
		return rowSum;
	}

	private static LongDecimal DivideInternal(LongDecimal x, LongDecimal y, int maxMantissaLength)
	{
		var MantissaOverflow = MpuTPowerOf10(maxMantissaLength);
		var quotient = (MantissaOverflow + (x.m >> 1)).ShiftLeftDec(maxMantissaLength + 1) / (MantissaOverflow + (y.m >> 1));
		var shiftAmount = quotient.DecLength - maxMantissaLength - 1;
		quotient = quotient.ShiftRightRoundDec(shiftAmount) - MantissaOverflow;
		if (x.e + shiftAmount >= y.e + 1)
			return new(quotient << 1, x.e - y.e + shiftAmount - 1, maxMantissaLength);
		else
			return new(quotient << 1 | 1, y.e - x.e - shiftAmount, maxMantissaLength);
	}

	private static LongDecimal DivideUiInternal(LongDecimal x, uint y, int MantissaLength)
	{
		var MantissaOverflow = MpuTPowerOf10(MantissaLength);
		var quotient = (MantissaOverflow + (x.m >> 1)).ShiftLeftDec(MantissaLength + 1) / y;
		var shiftAmount = MantissaLength * 2 + 2 - quotient.DecLength;
		var mantissa = (quotient.ShiftRightRoundDec(MantissaLength - shiftAmount + 1) - MantissaOverflow) << 1 | x.m & 1;
		if ((x.m & 1) != 0)
			return new(mantissa, x.e + shiftAmount, x.MantissaLength);
		else if (x.e >= shiftAmount)
			return new(mantissa, x.e - shiftAmount, x.MantissaLength);
		else
			return new(mantissa | 1, new UnsignedLongDecimal(shiftAmount) - x.e - 1, x.MantissaLength);
	}

