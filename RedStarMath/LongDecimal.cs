	{
		var mantissaOverflow = MpuTPowerOf10(mantissaLength);
		var mantissaMask = MpuTPowerOf10(mantissaLength) * 9 - 1;
		var xm = (x.m >> 1).ShiftLeftDec(xmlDiff);
		var ym = (y.m >> 1).ShiftLeftDec(ymlDiff);
		UnsignedLongDecimal newE;
		if ((x.m & 1) != 0)
		{
			if (Mpir.MpzCmpSi(ym, 0) >= 0)
			{
				var eDiff = y.e - x.e;
				if (eDiff == 0)
					return AddNear(xm, ym, x.e, mantissaLength, mantissaOverflow, mantissaMask);
				if (eDiff > mantissaLength)
					return x.GetWithOtherML(mantissaLength);
				var mSum = xm + (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
				if (Mpir.MpzCmp(mSum, mantissaMask) > 0)
				{
					var switchE = x.e == 0;
					newE = (switchE ? UnsignedLongDecimal.Zero : x.e - 1).GetWithOtherML(mantissaLength, false);
					var newM = (mantissaOverflow + mSum).ShiftRightRoundDec(1) - mantissaOverflow;
					return new(newM << 1 | (switchE ? 0 : 1), newE, mantissaLength);
				}
				newE = x.e.GetWithOtherML(mantissaLength, true);
				return new(mSum << 1 | 1, newE, mantissaLength);
			}
			ym = (~(y.m >> 1)).ShiftLeftDec(ymlDiff);
			if (x.e + 1 < y.e)
			{
				var eDiff = y.e - x.e;
				if (eDiff > mantissaLength)
					return x.GetWithOtherML(mantissaLength);
				var mDiff = mantissaOverflow + xm - (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
				if (Mpir.MpzCmp(mDiff, mantissaOverflow) >= 0)
					return new(mDiff - mantissaOverflow << 1 | 1, x.e.GetWithOtherML(mantissaLength, true), mantissaLength);
				newE = (x.e + 1).GetWithOtherML(mantissaLength, false);
				return new(mDiff * 10 - mantissaOverflow << 1 | 1, newE, mantissaLength);
			}
			else if (x.e == y.e)
			{
				var mDiff = xm - ym;
				if (mDiff == 0)
					return new(MpuTPowerOf10(mantissaLength) * 18 + 1, UnsignedLongDecimal.Zero, mantissaLength);
				var shiftAmount = mantissaLength - mDiff.DecLength + 1;
				newE = (x.e + shiftAmount).GetWithOtherML(mantissaLength, false);
				return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1 | 1, newE, mantissaLength);
			}
			else
			{
				var mDiff = (mantissaOverflow + xm) * 10 - (mantissaOverflow + ym);
				var shiftAmount = mantissaLength - mDiff.DecLength + 1;
				if (shiftAmount == -1)
				{
					newE = x.e.GetWithOtherML(mantissaLength, true);
					return new(mDiff.ShiftRightRoundDec(1) - mantissaOverflow << 1 | 1, newE, mantissaLength);
				}
				newE = (x.e + (shiftAmount + 1)).GetWithOtherML(mantissaLength, false);
				return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1 | 1, newE, mantissaLength);
			}
		}
		else if ((y.m & 1) != 0)
		{
			if (Mpir.MpzCmpSi(ym, 0) >= 0)
			{
				var eDiff = x.e + y.e + 1;
				if (eDiff > mantissaLength)
					return x.GetWithOtherML(mantissaLength);
				var mSum = xm + (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
				if (Mpir.MpzCmp(mSum, mantissaMask) > 0)
				{
					newE = x.e.GetWithOtherML(mantissaLength, false);
					var changeE = Mpir.MpzCmp(mSum, mantissaMask) > 0;
					var newM = changeE ? (mantissaOverflow + mSum).ShiftRightRoundDec(1) - mantissaOverflow : mSum;
					return new(newM << 1, changeE ? newE + 1 : newE, mantissaLength);
				}
				newE = x.e.GetWithOtherML(mantissaLength, true);
				return new(mSum << 1, newE, mantissaLength);
			}
			ym = (~(y.m >> 1)).ShiftLeftDec(ymlDiff);
			if (x.e != 0 || y.e != 0)
			{
				var eDiff = x.e + y.e + 1;
				if (eDiff > mantissaLength)
					return x.GetWithOtherML(mantissaLength);
				var mDiff = mantissaOverflow + xm - (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
				if (Mpir.MpzCmp(mDiff, mantissaOverflow) >= 0)
					return new(mDiff - mantissaOverflow << 1, x.e.GetWithOtherML(mantissaLength, true), mantissaLength);
				var changeE = x.e == 0;
				newE = (changeE ? UnsignedLongDecimal.Zero : x.e - 1).GetWithOtherML(mantissaLength, false);
				return new(mDiff * 10 - mantissaOverflow << 1 | (changeE ? 1 : 0), newE, mantissaLength);
			}
			else
			{
				var mDiff = (mantissaOverflow + xm) * 10 - (mantissaOverflow + ym);
				var shiftAmount = mantissaLength - mDiff.DecLength + 1;
				if (shiftAmount == -1)
				{
					newE = x.e.GetWithOtherML(mantissaLength, true);
					return new(mDiff.ShiftRightRoundDec(1) - mantissaOverflow << 1, newE, mantissaLength);
				}
				newE = new(shiftAmount, mantissaLength);
				return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1 | 1, newE, mantissaLength);
			}
		}
		else
		{
			if (Mpir.MpzCmpSi(ym, 0) >= 0)
			{
				var eDiff = x.e - y.e;
				MpzT mSum;
				if (eDiff == 0)
				{
					newE = x.e.GetWithOtherML(mantissaLength, false);
					mSum = xm + mantissaOverflow + ym;
					var changeE = Mpir.MpzCmp(mSum, mantissaMask) > 0;
					var newM = changeE ? (mantissaOverflow + mSum).ShiftRightRoundDec(1) - mantissaOverflow : mSum;
					return new(newM << 1, changeE ? newE + 1 : newE, mantissaLength);
				}
				if (eDiff > mantissaLength)
					return x.GetWithOtherML(mantissaLength);
				mSum = xm + (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
				if (Mpir.MpzCmp(mSum, mantissaMask) > 0)
				{
					newE = x.e.GetWithOtherML(mantissaLength, false);
					mSum = xm + (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
					var changeE = Mpir.MpzCmp(mSum, mantissaMask) > 0;
					var newM = changeE ? (mantissaOverflow + mSum).ShiftRightRoundDec(1) - mantissaOverflow : mSum;
					return new(newM << 1, changeE ? newE + 1 : newE, mantissaLength);
				}
				newE = x.e.GetWithOtherML(mantissaLength, true);
				return new(mSum << 1, newE, mantissaLength);
			}
			ym = (~(y.m >> 1)).ShiftLeftDec(ymlDiff);
			if (x.e > y.e + 1)
			{
				var eDiff = x.e - y.e;
				if (eDiff > mantissaLength)
					return x.GetWithOtherML(mantissaLength);
				var mDiff = mantissaOverflow + xm - (mantissaOverflow + ym).ShiftRightRoundDec((int)eDiff);
				if (Mpir.MpzCmp(mDiff, mantissaOverflow) >= 0)
					return new(mDiff - mantissaOverflow << 1, x.e.GetWithOtherML(mantissaLength, true), mantissaLength);
				newE = (x.e - 1).GetWithOtherML(mantissaLength, false);
				return new(mDiff * 10 - mantissaOverflow << 1, newE, mantissaLength);
			}
			else if (x.e == y.e)
			{
				var mDiff = xm - ym;
				if (mDiff == 0)
					return new(MpuTPowerOf10(mantissaLength) * 18 + 1, UnsignedLongDecimal.Zero, mantissaLength);
				var shiftAmount = mantissaLength - mDiff.DecLength + 1;
				if (x.e < shiftAmount)
				{
					newE = new(shiftAmount - (int)x.e - 1, mantissaLength);
					return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1 | 1, newE, mantissaLength);
				}
				newE = (x.e - shiftAmount).GetWithOtherML(mantissaLength, false);
				return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1, newE, mantissaLength);
			}
			else
			{
				var mDiff = (mantissaOverflow + xm) * 10 - (mantissaOverflow + ym);
				var shiftAmount = mantissaLength - mDiff.DecLength + 1;
				if (shiftAmount == -1)
				{
					newE = x.e.GetWithOtherML(mantissaLength, true);
					return new(mDiff.ShiftRightRoundDec(1) - mantissaOverflow << 1, newE, mantissaLength);
				}
				if (x.e <= shiftAmount)
				{
					newE = new(shiftAmount - (int)x.e, mantissaLength);
					return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1 | 1, newE, mantissaLength);
				}
				newE = (x.e - (shiftAmount + 1)).GetWithOtherML(mantissaLength, false);
				return new(mDiff.ShiftLeftDec(shiftAmount) - mantissaOverflow << 1, newE, mantissaLength);
			}
		}
	}

	private static LongDecimal AddNear(MpzT xm, MpzT ym, UnsignedLongDecimal e, int mantissaLength,
		MpzT mantissaOverflow, MpzT mantissaMask)
	{
		var mSum = xm + mantissaOverflow + ym;
		var changeE = Mpir.MpzCmp(mSum, mantissaMask) > 0;
		var switchE = e == 0 && changeE;
		UnsignedLongDecimal newE;
		if (switchE)
			newE = ((UnsignedLongDecimal)0).GetWithOtherML(mantissaLength, false);
		else
			newE = (e - (changeE ? 1 : 0)).GetWithOtherML(mantissaLength, false);
		var newM = changeE ? (mantissaOverflow + mSum).ShiftRightRoundDec(1) - mantissaOverflow : mSum;
		return new(newM << 1 | (switchE ? 0 : 1), newE, mantissaLength);
	}

	/// <summary>
	/// Вычисляет
	/// <see href="https://ru.wikipedia.org/wiki/Арифметико-геометрическое_среднее">арифметико-геометрическое среднее</see>
	/// указанных чисел.
	/// </summary>
	/// <param name="x">Первое число для вычисления арифметико-геометрического среднего.</param>
	/// <param name="y">Второе число для вычисления арифметико-геометрического среднего.</param>
	/// <returns>
	/// Если среди чисел есть неопределенность - неопределенность;<br />
	/// если одно число - ноль, а другое - бесконечность какого-либо знака - неопределенность;<br />
	/// есть ноль, но нет бесконечности - ноль;<br />
	/// бесконечность разных знаков - неопределенность;<br />
	/// бесконечность одного знака и число другого знака - неопределенность;<br />
	/// две бесконечности одного знака - бесконечность этого же знака;<br />
	/// бесконечность какого-либо знака и число этого же знака - бесконечность этого же знака;<br />
	/// числа разных знаков - неопределенность;<br />
	/// конечные числа одного знака - арифметико-геометрическое среднее этих чисел (подробнее см. по ссылке вsыше).
	/// </returns>
	public static LongDecimal AGM(LongDecimal x, LongDecimal y)
	{
		var maxMantissaLength = Math.Max(x.MantissaLength, y.MantissaLength);
		MpzT shiftedMantissaOverflow = MpuTPowerOf10(maxMantissaLength) * 18;
		if (Mpir.MpzCmp(x.m, x.NaNMantissa) == 0 || Mpir.MpzCmp(y.m, y.NaNMantissa) == 0)
			return new(shiftedMantissaOverflow + 4, UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmp(x.m, x.ZeroMantissa) == 0 || Mpir.MpzCmp(y.m, y.ZeroMantissa) == 0)
			return new(shiftedMantissaOverflow
				+ (Mpir.MpzCmp(x.m, x.PositiveInfinityMantissa) == 0 || Mpir.MpzCmp(x.m, x.NegativeInfinityMantissa) == 0
				|| Mpir.MpzCmp(y.m, y.PositiveInfinityMantissa) == 0 || Mpir.MpzCmp(y.m, y.NegativeInfinityMantissa) == 0
				? 4 : 1), UnsignedLongDecimal.Zero, maxMantissaLength);
		else if ((Mpir.MpzCmp(x.m, x.PositiveInfinityMantissa) == 0 || Mpir.MpzCmp(x.m, x.NegativeInfinityMantissa) == 0)
			&& (Mpir.MpzCmp(y.m, y.PositiveInfinityMantissa) == 0 || Mpir.MpzCmp(y.m, y.NegativeInfinityMantissa) == 0))
			return new(Mpir.MpzCmp(x.m, y.m) == 0 ? x.m : shiftedMantissaOverflow + 4,
				UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmp(x.m, x.PositiveInfinityMantissa) == 0)
			return new(shiftedMantissaOverflow + (Mpir.MpzCmpSi(y.m, 0) < 0 ? 4 : 2),
				UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmp(x.m, x.NegativeInfinityMantissa) == 0)
			return new(shiftedMantissaOverflow + (Mpir.MpzCmpSi(y.m, 0) < 0 ? 3 : 4),
				UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmp(y.m, y.PositiveInfinityMantissa) == 0)
			return new(shiftedMantissaOverflow + (Mpir.MpzCmpSi(x.m, 0) < 0 ? 4 : 2),
				UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmp(y.m, y.NegativeInfinityMantissa) == 0)
			return new(shiftedMantissaOverflow + (Mpir.MpzCmpSi(x.m, 0) < 0 ? 3 : 4),
				UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmpSi(x.m, 0) < 0 ^ Mpir.MpzCmpSi(y.m, 0) < 0)
			return new(shiftedMantissaOverflow + 4, UnsignedLongDecimal.Zero, maxMantissaLength);
		else if (Mpir.MpzCmpSi(x.m, 0) == 0 && x.e == 0 && Mpir.MpzCmpSi(y.m, 0) == 0 && y.e == 0)
			return new(MpzT.Zero, UnsignedLongDecimal.Zero, maxMantissaLength);
		x = x.GetWithOtherML(maxMantissaLength);
		y = y.GetWithOtherML(maxMantissaLength);
		if (Mpir.MpzCmp(x.m, y.m) == 0 && x.e == y.e)
			return new(x.m, x.e, maxMantissaLength);
		if (y > x)
			(x, y) = (y, x);
		UnsignedLongDecimal shiftAmount = new(MpuT.Zero, null, maxMantissaLength);
		if ((x.m & 1) != 0)
			shiftAmount = x.e + 1;
		if ((y.m & 1) != 0 && y.e >= shiftAmount)
			shiftAmount = y.e + 1;
		if (Mpir.MpzCmpSi(x.m, 0) < 0)
			return -AGMInternal(-y << shiftAmount, -x << shiftAmount, maxMantissaLength) >> shiftAmount;
		else
			return AGMInternal(x << shiftAmount, y << shiftAmount, maxMantissaLength) >> shiftAmount;
	}

	private static LongDecimal AGMInternal(LongDecimal x, LongDecimal y, int maxMantissaLength)
	{
		LongDecimal a = x, b = y;
		do
			(a, b) = (AddInternal(a, b, maxMantissaLength) / 2, GeometricMeanInternal(a, b, maxMantissaLength));
		while (a.e != b.e || Mpir.MpzCmpabsUi((a.m >> 1) - (b.m >> 1), 9) > 0);
		return a;
	}

	/// <summary>
	/// Вычисляет арксинус данного числа.
	/// </summary>
	/// <returns>
	/// Для нуля - ноль;<br />
	/// для плюс бесконечности, минус бесконечности и неопределенности - неопределенность;<br />
	/// для чисел, модуль которых больше 1 - неопределенность;<br />
	/// в остальных случаях - арксинус данного числа.
	/// </returns>
	public LongDecimal Asin()
	{
		if (Mpir.MpzCmp(m, ZeroMantissa) == 0)
			return new(ShiftedMantissaOverflow + 1, UnsignedLongDecimal.Zero, MantissaLength);
		else if (Mpir.MpzCmp(m, ShiftedMantissaOverflow) > 0)
			return new(ShiftedMantissaOverflow + 4, UnsignedLongDecimal.Zero, MantissaLength);
		else if ((m & 1) == 0)
		{
			if (e != 0)
				return new(ShiftedMantissaOverflow + 4, UnsignedLongDecimal.Zero, MantissaLength);
			else if (Mpir.MpzCmpSi(m, 0) == 0)
				return Pi.GetWithOtherML(MantissaLength) / 2;
			else if (Mpir.MpzCmpSi(m, -2) == 0)
				return -Pi.GetWithOtherML(MantissaLength) / 2;
			else
				return new(ShiftedMantissaOverflow + 4, UnsignedLongDecimal.Zero, MantissaLength);
		}
		var sign = Mpir.MpzCmpSi(m, 0) < 0 ? -1 : 1;
		var localValue = Abs().GetWithOtherML(MantissaLength + 100);
		var threshold = new LongDecimal(MpuTPowerOf10(MantissaLength + 100) << 1, UnsignedLongDecimal.Zero,
			MantissaLength + 100);
		threshold = threshold.SqrtInternal().ReciprocInternal();
		if (localValue == threshold)
			return (2 - sign) * Pi.GetWithOtherML(MantissaLength) / 4;
		var reverse = localValue > threshold;
		if (!reverse)
			localValue = AddInternal(One, -localValue.ReciprocInternal().SquareInternal().ReciprocInternal(),
				MantissaLength + 100).ReciprocInternal().SqrtInternal().ReciprocInternal();
		localValue = AddInternal(One, -AddInternal(One, -localValue, MantissaLength + 100) / 2,
			MantissaLength + 100).ReciprocInternal().SqrtInternal().ReciprocInternal();
		localValue = AddInternal(One, -localValue.ReciprocInternal().SquareInternal().ReciprocInternal(),
			MantissaLength + 100).ReciprocInternal().SqrtInternal().ReciprocInternal();
		localValue = localValue.AsinInternal() * 2;
		if (reverse)
			localValue = Pi.GetWithOtherML(MantissaLength + 100) / 2 - localValue;
		return (localValue * sign).GetWithOtherML(MantissaLength);
	}

