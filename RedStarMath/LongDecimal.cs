	/// для целых чисел - данное число;<br />
	/// в остальных случаях - см. общее описание.
	/// </returns>
	public LongDecimal Truncate()
	{
		if (Mpir.MpzCmp(m, ShiftedMantissaOverflow) > 0)
			return this;
		if ((m & 1) != 0)
			return new(ShiftedMantissaOverflow + 1, UnsignedLongDecimal.Zero, MantissaLength);
		if (e >= MantissaLength)
			return this;
		var newM = m >> 1;
		if (Mpir.MpzCmpSi(m, 0) < 0)
			newM = ~newM;
		var shiftAmount = MantissaLength - (int)e;
		newM = newM.ShiftRightDec(shiftAmount).ShiftLeftDec(shiftAmount);
		if (Mpir.MpzCmpSi(m, 0) < 0)
			newM = ~newM;
		return new(newM << 1, e, MantissaLength);
	}

