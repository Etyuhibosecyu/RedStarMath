	public LongDecimal(UnsignedLongDecimal op, int mantissaLength = AutoMantissaLength)
	{
		if (mantissaLength == AutoMantissaLength)
			mantissaLength = op.MantissaLength;
		else
		{
			if (mantissaLength is < MinMantissaLength or > int.MaxValue)
				mantissaLength = DefaultMantissaLength;
			op = op.GetWithOtherML(mantissaLength, false);
		}
		MantissaLength = mantissaLength;
		if (op.e is null)
		{
			if (Mpir.MpuCmpSi(op.m, 0) == 0)
				m = ShiftedMantissaOverflow + 1;
			else
				m = ShiftUniversal(Unsafe.As<MpzT>(op.m), MantissaLength - op.m.DecLength + 1) - MantissaOverflow << 1;
			e = new(op.m.DecLength - 1);
		}
		else
		{
			m = Unsafe.As<MpzT>(op.m) << 1;
			e = op.e + (MantissaLength - 1);
		}
	}

	public LongDecimal(LongReal op)
	{
		MantissaLength = op.MantissaLength;
		var otherShiftedMantissaOverflow = MpzT.One << MantissaLength + 1;
		if (Mpir.MpzCmp(op.m, otherShiftedMantissaOverflow) > 0)
		{
			m = op.m - otherShiftedMantissaOverflow + ShiftedMantissaOverflow;
			e = new(MpuT.Zero, null, MantissaLength);
			return;
		}
		var mantissa = op.m >> 1;
		if (Mpir.MpzCmpSi(mantissa, 0) < 0)
			mantissa = ~mantissa;
		mantissa += MpzT.One << MantissaLength;
		mantissa *= MpuTPowerOf5(MantissaLength);
		mantissa -= MantissaOverflow;
		if (Mpir.MpzCmpSi(op.m, 0) < 0)
			mantissa = ~mantissa;
		if ((op.m & 1) == 0 && op.e == UnsignedLongReal.Zero)
		{
			m = mantissa << 1;
			e = new(MpuT.Zero, null, MantissaLength);
			return;
		}
		var exponent = new LongReal(op.e, MantissaLength);
		var ld = new LongDecimal(mantissa << 1, UnsignedLongDecimal.Zero, MantissaLength);
		if (exponent.e < 31)
			ld *= Two.GetWithOtherML(MantissaLength).Power((op.m & 1) != 0 ? ~(int)exponent : (int)exponent);
		else
			ld *= PowerOf2(new((op.m & 1) != 0 ? ~exponent : exponent));
		m = ld.m;
		e = ld.e;
	}

